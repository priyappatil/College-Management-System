    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String s1 = jTextField6.getText();
        String s2 = jTextField7.getText();
        if(!s2.contains("@"))
        {
            JOptionPane.showMessageDialog(this,"Please provide proper emailid","Error",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            if(s1.length() != 10)
            {
                JOptionPane.showMessageDialog(this,"Please provide proper contact number","Error",JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                String id = jTextField1.getText();
                String firstname = jTextField2.getText();
                String lastname = jTextField3.getText();
                String position = jTextField4.getText();
                String age = jTextField5.getText();
                String contact = jTextField6.getText();
                String emailid = jTextField7.getText();
                String gender = (String) jComboBox1.getSelectedItem();
                String permanentaddress = jTextArea1.getText();
                String presentaddress = jTextArea2.getText();

                try
                {
                    String sql = "update employee set firstname = '"+firstname+"', lastname = '"+lastname+"', position = '"+position+"', age = '"+age+"', contact = '"+contact+"', emailid = '"+emailid+"', gender = '"+gender+"', permanentaddress = '"+permanentaddress+"', presentaddress = '"+presentaddress+"' where id = "+id+" ";
                    int iRet = st.executeUpdate(sql);

                    if(iRet == 1)
                    {
                        JOptionPane.showMessageDialog(this,"Updated Successfully","Message",JOptionPane.INFORMATION_MESSAGE);
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                        jTextField5.setText("");
                        jTextField6.setText("");
                        jTextField7.setText("");
                        jComboBox1.setName("");
                        jTextArea1.setText("");
                        jTextArea2.setText("");
                        jCheckBox1.setSelected(false);
                    }
                }
                catch(Exception E)
                {
                    JOptionPane.showMessageDialog(this, E.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

