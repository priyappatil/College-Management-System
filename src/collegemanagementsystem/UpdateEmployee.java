    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        try
        {
            String sql = "select * from employee where id = "+id+" ";
            rs = st.executeQuery(sql);

            if(rs.next() == true)
            {
                jTextField2.setText(rs.getString("firstname"));
                jTextField3.setText(rs.getString("lastname"));
                jTextField4.setText(rs.getString("position"));
                jTextField5.setText(rs.getString("age"));
                jTextField6.setText(rs.getString("contact"));
                jTextField7.setText(rs.getString("emailid"));
                jComboBox1.setSelectedItem(rs.getString("gender"));
                jTextArea1.setText(rs.getString("permanentaddress"));
                jTextArea2.setText(rs.getString("presentaddress"));
            }
            else
            {
                JOptionPane.showMessageDialog(this,"No record found for id : "+id,"Error",JOptionPane.ERROR_MESSAGE);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
                jTextArea1.setText("");
                jTextArea2.setText("");
                jComboBox1.setSelectedItem(-1);
                jCheckBox1.setSelected(false);
            }
        }
        catch(Exception E)
        {
            JOptionPane.showMessageDialog(this,"Please provide proper id");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

